module.exports = (month) => {
	switch (month) {
	case 'January':
		return "01";
		break;
	case 'February':
		return "02";
		break;
	case 'March':
		return "03";
		break;
	case 'April':
		return "04";
		break;
	case 'May':
		return "05";
		break;
	case 'June':
		return "06";
		break;
	case 'July':
		return "07";
		break;
	case 'August':
		return "08";
		break;
	case 'September':
		return "09";
		break;
	case 'October':
		return "10";
		break;
	case 'November':
		return "11";
		break;
	case 'December':
		return "12";
		break;
	}
};
